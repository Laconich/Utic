// Crear variables para tres números: a, b, c
var a = 10;
var b = 5;
var c = 3;

// Sumar b en a
a += b;

// Dividir a con c
a /= c;

// Reemplazar el valor de c con el módulo de c y b
c = c % b;

// Imprimir los tres números en consola
console.log("a: " + a);
console.log("b: " + b);
console.log("c: " + c);
